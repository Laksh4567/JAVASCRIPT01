//Primitive Data Type: Numbers in JavaScript are a primitive data type.
//Integer and Floating-Point: JavaScript uses both integers and floating-point numbers.
// NaN (Not-a-Number): Represents a value that is not a legal number.
//Infinity: Represents a number that is too large to handle.


//*****Number Methods
//Number(): Converts a value to a number.
//parseInt(): Parses a string and returns an integer.
//parseFloat(): Parses a string and returns a floating-point number.
//toFixed(): Formats a number with a fixed number of decimal places.
//toPrecision(): Formats a number to a specified length.
//toString(): Converts a number to a string.



//************Math Object
// Math.PI: Returns the value of PI (Ï€).
// Math.E: Returns the value of Euler's number (e).
// Math.sqrt(): Returns the square root of a number.
// Math.pow(): Returns the base to the exponent power.
// Math.abs(): Returns the absolute value of a number.
// Math.round(): Rounds a number to the nearest integer.
// Math.ceil(): Rounds a number up to the nearest integer.
// Math.floor(): Rounds a number down to the nearest integer.
// Math.min(): Returns the smallest of zero or more numbers.
// Math.max(): Returns the largest of zero or more numbers.
// Math.random(): Returns a random number between 0 (inclusive) and 1 (exclusive).


//********Special Number Values
// Number.MAX_VALUE: The largest positive representable number.
// Number.MIN_VALUE: The smallest positive representable number.
// Number.POSITIVE_INFINITY: Represents positive infinity.
// Number.NEGATIVE_INFINITY: Represents negative infinity.


//********Common Use Cases
// Rounding Numbers: Use Math.round(), Math.ceil(), or Math.floor().
// Generating Random Numbers: Use Math.random().
// Converting Strings to Numbers: Use Number(), parseInt(), or parseFloat().
// Formatting Numbers: Use toFixed() or toPrecision().


